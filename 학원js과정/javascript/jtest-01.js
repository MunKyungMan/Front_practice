// 1. 클릭하면 1회 색상 변하기
var head = document.querySelector("#heading");
head.onclick = function(){
    head.style.color = "red";
}

var hr_line = document.querySelector("#line");
hr_line.onclick = function(){
    hr_line.style.border = "20px solid blue";
}

